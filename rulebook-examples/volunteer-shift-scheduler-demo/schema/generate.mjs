#!/usr/bin/env node
// Reads schema/scheduler.schema.mjs and emits:
//   db/schema.sql              — DDL (tables, FKs, indexes, drops)
//   generated/types.d.ts       — TS types for every table + computed field
//   generated/dag.json         — DAG metadata (used by /api/schema and the UI)
//   generated/schema.meta.json — convenience dump consumed by the Excel exporter

import { writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { tables, computed, computedByTable, computedOrder, constants } from './scheduler.schema.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, '..');

const banner = (subject) =>
  `-- AUTOGENERATED from schema/scheduler.schema.mjs — do not edit by hand.\n` +
  `-- Regenerate with: ./start.sh build   (or: node schema/generate.mjs)\n` +
  `-- Subject: ${subject}\n`;

const tsBanner =
  `// AUTOGENERATED from schema/scheduler.schema.mjs — do not edit by hand.\n` +
  `// Regenerate with: ./start.sh build   (or: node schema/generate.mjs)\n\n`;

// ---------------------------------------------------------------------------
// DDL
// ---------------------------------------------------------------------------
function ddlForColumn(name, col) {
  const parts = [name, col.type.toUpperCase()];
  if (col.primaryKey) parts.push('PRIMARY KEY');
  if (col.notNull) parts.push('NOT NULL');
  if (col.unique) parts.push('UNIQUE');
  if (col.default !== undefined) parts.push(`DEFAULT ${col.default}`);
  if (col.check) parts.push(`CHECK (${col.check})`);
  if (col.references) {
    const onDel = col.references.onDelete ? ` ON DELETE ${col.references.onDelete}` : '';
    parts.push(`REFERENCES ${col.references.table}(${col.references.column})${onDel}`);
  }
  return parts.join(' ');
}

function ddlForTable(tableName, spec) {
  const cols = Object.entries(spec.columns).map(([n, c]) => '  ' + ddlForColumn(n, c));
  const lines = [...cols];
  if (spec.compoundPrimaryKey) {
    lines.push(`  PRIMARY KEY (${spec.compoundPrimaryKey.join(', ')})`);
  }
  for (const u of spec.compoundUnique || []) {
    lines.push(`  UNIQUE (${u.join(', ')})`);
  }
  return `-- ${spec.description}\nCREATE TABLE ${tableName} (\n${lines.join(',\n')}\n);\n`;
}

function emitSql() {
  const dropOrder = Object.keys(tables).slice().reverse();
  const drops = dropOrder.map((t) => `DROP TABLE IF EXISTS ${t} CASCADE;`).join('\n');
  const creates = Object.entries(tables)
    .map(([n, s]) => ddlForTable(n, s))
    .join('\n');
  const indexes = Object.entries(tables)
    .flatMap(([t, s]) =>
      (s.indexes || []).map((ix) => `CREATE INDEX ${ix.name} ON ${t}(${ix.columns.join(', ')});`),
    )
    .join('\n');

  const sql =
    banner('Table schema (DDL)') +
    '\n' +
    drops +
    '\n\n' +
    creates +
    '\n' +
    indexes +
    '\n';
  writeFileSync(resolve(ROOT, 'db', 'schema.sql'), sql);
}

// ---------------------------------------------------------------------------
// TypeScript types
// ---------------------------------------------------------------------------
const sqlTypeToTs = (sqlType) => {
  const t = String(sqlType).toLowerCase();
  if (t.startsWith('serial') || t.startsWith('int')) return 'number';
  if (t.startsWith('numeric')) return 'number';
  if (t.startsWith('text')) return 'string';
  if (t.startsWith('date') || t.startsWith('timestamp')) return 'string'; // ISO string from pg
  return 'unknown';
};

const returnsToTs = (returns) => {
  if (!returns) return 'unknown';
  const r = String(returns).toLowerCase();
  if (r.startsWith('integer') || r.startsWith('numeric')) return r.includes('null') ? 'number | null' : 'number';
  if (r.startsWith('text')) return r.includes('null') ? 'string | null' : 'string';
  if (r.startsWith('boolean')) return 'boolean';
  return 'unknown';
};

function emitTypes() {
  let out = tsBanner;
  out += `// ----- Table row types ------------------------------------------------------\n\n`;
  for (const [tableName, spec] of Object.entries(tables)) {
    const interfaceName = pascal(tableName) + 'Row';
    out += `/** ${spec.description} */\n`;
    out += `export interface ${interfaceName} {\n`;
    for (const [colName, col] of Object.entries(spec.columns)) {
      const tsType = sqlTypeToTs(col.type);
      const optional = col.notNull || col.primaryKey ? '' : '?';
      out += `  ${colName}${optional}: ${tsType};\n`;
    }
    out += `}\n\n`;
  }

  out += `// ----- Computed field shapes ------------------------------------------------\n\n`;
  // Group computed fields by their target table (events/shifts/volunteer_event).
  for (const [target, keys] of Object.entries(computedByTable)) {
    const interfaceName = pascal(target) + 'Computed';
    out += `/** Derived fields for ${target} */\n`;
    out += `export interface ${interfaceName} {\n`;
    for (const key of keys) {
      const f = computed[key];
      const field = key.split('.').slice(1).join('.');
      const tsType = returnsToTs(f.returns);
      out += `  /** ${f.formula.replace(/\*\//g, '* /')} */\n`;
      out += `  ${field}: ${tsType};\n`;
    }
    out += `}\n\n`;
  }

  out += `// ----- DAG node descriptor (matches generated/dag.json) ---------------------\n\n`;
  out += `export interface DagNode {\n`;
  out += `  key: string;\n`;
  out += `  kind: 'raw' | 'lookup' | 'aggregation' | 'calc';\n`;
  out += `  table: string;\n`;
  out += `  field: string;\n`;
  out += `  description?: string;\n`;
  out += `  formula?: string;\n`;
  out += `  excelFormula?: string | null;\n`;
  out += `  sqlFormula?: string | null;\n`;
  out += `  depends: string[];\n`;
  out += `  constants?: string[];\n`;
  out += `}\n\n`;

  out += `export type DagGraph = Record<string, DagNode>;\n`;

  writeFileSync(resolve(ROOT, 'generated', 'types.d.ts'), out);
}

function pascal(s) {
  return s
    .split(/[_.]/)
    .map((p) => p.charAt(0).toUpperCase() + p.slice(1))
    .join('');
}

// ---------------------------------------------------------------------------
// DAG metadata (one node per raw column AND one node per computed field)
// ---------------------------------------------------------------------------
function emitDag() {
  /** @type {Record<string, any>} */
  const graph = {};

  // Raw columns
  for (const [tableName, spec] of Object.entries(tables)) {
    for (const colName of Object.keys(spec.columns)) {
      const key = `${tableName}.${colName}`;
      graph[key] = {
        key,
        kind: 'raw',
        table: tableName,
        field: colName,
        description: `Raw column ${tableName}.${colName} (${spec.columns[colName].type}).`,
        formula: null,
        excelFormula: null,
        sqlFormula: null,
        depends: [],
      };
    }
  }

  // Computed fields
  for (const [key, f] of Object.entries(computed)) {
    const [table, field] = key.split('.');
    graph[key] = {
      key,
      kind: f.kind,
      table,
      field,
      description: f.description,
      formula: f.formula,
      excelFormula: f.excelFormula || null,
      sqlFormula: f.sqlFormula || null,
      depends: f.depends || [],
      constants: f.constants || [],
    };
  }

  const meta = {
    generatedAt: new Date().toISOString(),
    tableCount: Object.keys(tables).length,
    computedCount: Object.keys(computed).length,
    rawColumnCount: Object.values(tables).reduce((n, t) => n + Object.keys(t.columns).length, 0),
    computedOrder,
    constants,
  };

  writeFileSync(
    resolve(ROOT, 'generated', 'dag.json'),
    JSON.stringify({ ...meta, graph }, null, 2),
  );

  // Also emit a slimmer convenience dump for the Excel exporter.
  writeFileSync(
    resolve(ROOT, 'generated', 'schema.meta.json'),
    JSON.stringify(
      {
        generatedAt: meta.generatedAt,
        tables: Object.fromEntries(
          Object.entries(tables).map(([n, t]) => [
            n,
            {
              description: t.description,
              columns: Object.entries(t.columns).map(([colName, c]) => ({
                name: colName,
                type: c.type,
                primaryKey: !!c.primaryKey,
                notNull: !!c.notNull,
                unique: !!c.unique,
                default: c.default ?? null,
                check: c.check ?? null,
                references: c.references ?? null,
              })),
            },
          ]),
        ),
        computed: Object.entries(computed).map(([key, f]) => {
          const [t, field] = key.split('.');
          return {
            key,
            table: t,
            field,
            kind: f.kind,
            returns: f.returns,
            description: f.description,
            formula: f.formula,
            excelFormula: f.excelFormula || null,
            sqlFormula: f.sqlFormula || null,
            depends: f.depends || [],
            constants: f.constants || [],
          };
        }),
        constants,
      },
      null,
      2,
    ),
  );
}

// ---------------------------------------------------------------------------
function ensureDir(p) {
  mkdirSync(resolve(ROOT, p), { recursive: true });
}

ensureDir('db');
ensureDir('generated');

emitSql();
emitTypes();
emitDag();

console.log('schema/generate.mjs:');
console.log('  wrote db/schema.sql');
console.log('  wrote generated/types.d.ts');
console.log('  wrote generated/dag.json');
console.log('  wrote generated/schema.meta.json');
console.log(
  `  ${Object.keys(tables).length} tables, ${Object.keys(computed).length} computed fields, topo order: ${computedOrder.length}`,
);
