#!/usr/bin/env python3
"""Cross-substrate conformance referee for Simpson's Paradox derived fields.

Two independent substrates compute the SAME formula fields from the SAME leaf
inputs (CaseCells.Successes, CaseCells.Cases, StratumVariables raw fields, etc.):

  * Postgres : vw_treatment_rankings, vw_stratum_summaries, vw_stratum_variables
               (generated by rulebook-to-postgres from the rulebook formulas)
  * OWL-SHACL: rules.shacl.ttl SPARQL-CONSTRUCT rules run over individuals.ttl
               (generated by rulebook-to-owl from the same rulebook formulas)

The transpiler emits SHACL-SPARQL rules for ALL field types including
COUNTIFS/SUMIFS aggregations (sub-SELECT COUNT/SUM/AVG over child individuals),
arithmetic combinations of aggregations (SUMIFS/COUNTIFS ratio patterns), and
IF(agg=0, "", agg) guard-wrapped aggregation formulas. The conformance set is
the full set of formula-derived fields across TreatmentRankings, StratumSummaries,
and StratumVariables.

Strategy: load the individuals graph, run SHACL fixpoint derivation to let the
OWL reasoner compute all formula fields (aggregations + pure formulas), then
compare against Postgres.

This is the same pattern as the causal-autoimmune-architecture: load the
leaves, derive the full DAG, diff the two substrates.

Deps: rdflib, pyshacl, psycopg2 (all present in the ERB environment).
Run:  python3 owl/reason.py   (from the project root)
"""
