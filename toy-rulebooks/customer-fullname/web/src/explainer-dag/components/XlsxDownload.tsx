// Small Excel-icon link that downloads the project's effortless-rulebook.xlsx.
//
// Convention: the host app's effortless.json wires `rulebook-to-xlsx` as a
// chained step that drops `{ProjectName}-rulebook.xlsx` into the SPA's static
// asset dir (e.g. `public/`). This component just links to it.
//
// The default filename is derived from the rulebook's `Name` field (baked in
// at transpile time). Override via the `filename` prop if needed.

import type { CSSProperties } from "react";
import { rulebook } from "../embedded-rulebook.ts";

export interface XlsxDownloadProps {
  /** Override the link target. Defaults to `${BASE_URL}{ProjectName}-rulebook.xlsx`. */
  href?: string;
  /** Override the download filename. Defaults to `{ProjectName}-rulebook.xlsx`. */
  filename?: string;
  /** Inline style overrides. */
  style?: CSSProperties;
}

function projectName(): string {
  const r = rulebook as Record<string, unknown>;
  return typeof r.Name === "string" && r.Name.trim() ? r.Name.trim() : "rulebook";
}

function defaultFilename(): string {
  return `${projectName()}-rulebook.xlsx`;
}

function defaultHref(fname: string): string {
  // Vite injects import.meta.env.BASE_URL at build time. Guard for non-Vite hosts.
  const base =
    typeof import.meta !== "undefined" &&
    (import.meta as any).env &&
    (import.meta as any).env.BASE_URL
      ? (import.meta as any).env.BASE_URL
      : "/";
  const trimmed = base.endsWith("/") ? base : base + "/";
  return `${trimmed}${fname}`;
}

export function XlsxDownload({
  href,
  filename,
  style,
}: XlsxDownloadProps): JSX.Element {
  const fname = filename ?? defaultFilename();
  const target = href ?? defaultHref(fname);
  return (
    <a
      href={target}
      download={fname}
      className="dag-xlsx-download"
      title={`Download the live effortless-rulebook spreadsheet (${fname}). Generated by the rulebook-to-xlsx transpiler.`}
      aria-label="Download rulebook spreadsheet"
      style={style}
    >
      {/* Inline SVG so no asset pipeline is required. Green Excel-ish glyph. */}
      <svg
        width="16"
        height="16"
        viewBox="0 0 24 24"
        fill="none"
        xmlns="http://www.w3.org/2000/svg"
        aria-hidden="true"
      >
        <rect x="3" y="3" width="18" height="18" rx="2" fill="#1F7244" />
        <path
          d="M8 8l8 8M16 8l-8 8"
          stroke="#FFFFFF"
          strokeWidth="2"
          strokeLinecap="round"
        />
      </svg>
      <span className="dag-xlsx-label">XLSX</span>
    </a>
  );
}
